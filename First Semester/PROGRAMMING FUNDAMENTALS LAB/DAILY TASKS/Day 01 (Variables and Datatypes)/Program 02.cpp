#include <iostream>

using namespace std;
int main ()
	{
		
	// ASSIGNING THE VALUES OF THE TWO INTEGERS:	
	
	int num1 = 10;
	int num2 = 2;
	
	// ASSIGNING THE VALUES OF THE TWO DECIMALS:
	
	float decimal1 = 6.5;
	float decimal2 = 3.5;
	
	//PRINTING VALUES OF THE INTEGERS AND DECIMALS:
	
	cout << "\n	FIRST INTEGER  = 10 " <<	"\t\tFIRST DECIMAL  = 6.5 " << endl;
	cout << "	SECOND INTEGER = 2 " << "		SECOND DECIMAL = 3.5 " << endl << endl;

	// PERFORMING AND PRINTING OPERATIONS ON THE TWO INTEGERS:
	
	cout << "\n		SUM OF THE TWO INTEGERS		:	" << num1 + num2 << endl;
	cout << " 		DIFFERENCE OF THE TWO INTEGERS 	:	" << num1 - num2 << endl;
	cout << " 		PRODUCT OF THE TWO INTEGERS 	:	" << num1 * num2 << endl;
	cout << " 		DIVISION OF THE TWO INTEGERS 	:	" << num1 / num2 << endl << endl;

	// PERFORMING AND PRINTING OPERATIONS ON THE TWO DECIMALS:
	
	cout << "\n		SUM OF THE TWO DECIMALS 	:	" << decimal1 + decimal2 << endl;
	cout << "		DIFFERENCE OF THE TWO DECIMALS 	:	" << decimal1 - decimal2 << endl;
	cout << "		PRODUCT OF THE TWO DECIMALS 	:	" << decimal1 * decimal2 << endl;
	cout << "		DIVISION OF THE TWO DECIMALS 	:	" << decimal1 / decimal2 << endl;

	return 0;
	}
	
	
